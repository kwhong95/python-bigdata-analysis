# -*- coding: utf-8 -*-
# 정수
a = 123

# 실수 
a = 12.34

# 복소수 
a = 1 + 2j
print(a.real)
print(a.imag)
print(a.conjugate())
print(abs(a))

# 8진수
a = 0o12
print(a)

# 16진수
a = 0x12A
print(a)

# 논리형 - Bol
b = True
print(b)

# 숫자 연산

a = 3
b = 4

print(a + b)
print(a - b)
print(a * b)
print(a / b)
# 지수승 연산
print(a ** b)
print(a % b)
# 나눗셈 몫 소숫점 버림
print(a // b)
